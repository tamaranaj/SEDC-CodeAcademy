//Function declaration
//Function that sums 2 numbers

function sumsFunction(x,y){
    let result = x + y;
    console.log(result);
    return result;
}
//Function that subtracts 2 numbers
function subFunction(x,y){
    let result = x - y;
    console.log(result);
    return result;
}
//Function that multiplies 2 numbers
function multFunction(x,y){
    let result = x * y;
    console.log(result);
    return result;
}
//Function that divides 2 numbers
function divFunction(x,y){
    let result = x / y;
    console.log(result);
    return result;
}

//Calling functions
sumsFunction(8,7);
subFunction(22,13);
multFunction(30,6);
divFunction(64,5);